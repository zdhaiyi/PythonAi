# ==================== 第4步：添加追踪信息 ====================
def fib_with_trace(n, depth=0):
    """
    带追踪信息的fib函数，显示每一步的执行过程
    
    参数：
      n - 要计算的项数
      depth - 递归深度（用于格式化输出）
    """
    indent = "  " * depth  # 根据深度添加缩进
    
    print(f"{indent}→ 调用 fib({n})")
    
    if n == 1 or n == 2:
        print(f"{indent}  ✓ fib({n}) = 1 （终止条件，直接返回）")
        return 1
    else:
        print(f"{indent}  ? fib({n}) 需要计算 fib({n-1}) + fib({n-2})")
        
        # 先计算 fib(n-1)
        print(f"{indent}  ↓ 先计算 fib({n-1})")
        left = fib_with_trace(n - 1, depth + 1)
        
        # 再计算 fib(n-2)
        print(f"{indent}  ↓ 再计算 fib({n-2})")
        right = fib_with_trace(n - 2, depth + 1)
        
        result = left + right
        print(f"{indent}  ✓ fib({n}) = {left} + {right} = {result}")
        return result


print("=" * 70)
