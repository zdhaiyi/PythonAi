"""
题目：编写一个递归函数，打印汉诺塔的移动步骤
有 A、B、C 三根柱子，A 上有 n 个盘子（从小到大叠放）
要求：将所有盘子从 A 移动到 C，每次只能移动一个盘子，且大盘不能放在小盘上

解题思路：
1. 终止条件：当只有 1 个盘子时，直接从 A 移到 C
2. 递归关系（三步）：
   - 将上面 n-1 个盘子从 A 移到 B（借助 C）
   - 将最下面的盘子从 A 移到 C
   - 将 n-1 个盘子从 B 移到 C（借助 A）
3. 每次递归减少一个盘子，直到只剩一个
"""


