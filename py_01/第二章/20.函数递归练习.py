# ==================== 第4题：计算幂次方 ====================
"""
题目：编写一个递归函数，计算 base 的 exponent 次方
例如：2^5 = 2 × 2 × 2 × 2 × 2 = 32

解题思路：
1. 终止条件：当 exponent = 0 时，返回 1；当 exponent = 1 时，返回 base
2. 递归关系：power(base, exp) = base × power(base, exp - 1)
3. 每次递归指数减 1，直到达到终止条件
"""
def power(base,exponent):
    if exponent == 0:
        return 1
    elif exponent == 1:
        return base
    return base * power(base , exponent - 1)
print(power(2,5))

